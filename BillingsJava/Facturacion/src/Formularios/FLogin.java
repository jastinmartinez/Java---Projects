    public int validar_ingreso()
   {
        String usuario = TxtUsuario.getText();
        String clave = String.valueOf(TxtContrasena.getPassword());

        int resultado = 0;

        String Cadena = "select Usuario, Contrasena from InicioSesion where Usuario = '"+usuario+"' and Contrasena = '"+clave+"'";
        Connection conect = GetConexion();
        try 
        {          
            Statement st = conect.createStatement();
            ResultSet rs = st.executeQuery(Cadena);
            if(rs.next())
            {

            resultado = 1;

            }
        }
        catch (SQLException ex) 
        {
            
          JOptionPane.showMessageDialog(null, ex, "Error de conexi칩n", JOptionPane.ERROR_MESSAGE);

        }
        finally
        {
        try 
        {

            conect.close();

        } 
        catch (SQLException ex) 
        {

            JOptionPane.showMessageDialog(null, ex, "Error de desconexi칩n", JOptionPane.ERROR_MESSAGE);
        }
        
        }
      return resultado;
  }

